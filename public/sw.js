/**
 * Fired when the PWA is installed
 * Wait till event is invoked, then store all files into the "offline" cache
 */
self.addEventListener("install", (event) => {
    event.waitUntil(
        caches.open("offline").then((cache) => cache.addAll(filesToCache))
    );
});

const checkResponse = function (request) {
    return new Promise(function (fulfill, reject) {
        fetch(request).then(function (response) {
            if (response.status !== 404) {
                fulfill(response);
            } else {
                reject();
            }
        }, reject);
    });
};

const addToCache = async function (request) {
    const cache = await caches.open("offline");
    const response = await fetch(request);
    return await cache.put(request, response);
};

const returnFromCache = async function (request) {
    const cache = await caches.open("offline");
    const matching = await cache.match(request);
    if (!matching || matching.status === 404) {
        return cache.match("offline.html");
    } else {
        return matching;
    }
};

self.addEventListener("fetch", function (event) {
    event.respondWith(checkResponse(event.request).catch(function () {
        return returnFromCache(event.request);
    }));
    if (!event.request.url.startsWith('http')) {
        event.waitUntil(addToCache(event.request));
    }
});
